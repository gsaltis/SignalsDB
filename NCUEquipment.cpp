/*****************************************************************************!
 * Static Data
 *****************************************************************************/
QStringList
NCUEquipment::Tags = QStringList()
  << "TypeName"
  << "GroupName"
  << "NumofSamples"
  << "NumofCtrl"
  << "NumofSet"
  << "NumofAlarm"
  << "Related";

/*****************************************************************************!
 * Function : NCUEquipment
 *****************************************************************************/
NCUEquipment::NCUEquipment
() : NCUSignal()
{
  for ( auto tag : Tags ) {
    Values[tag] = QString();
  }
}

/*****************************************************************************!
 * Function : ~NCUEquipment
 *****************************************************************************/
NCUEquipment::~NCUEquipment
()
{
}

/*****************************************************************************!
 * Function : operator==
 *****************************************************************************/
bool
NCUEquipment::Equal
(NCUEquipment* InEquipment)
{
  for ( auto tag : Tags ) {
    if ( Values[tag] != InEquipment->GetValue(tag) ) {
      return false;
    }
  }
  return true;
}

