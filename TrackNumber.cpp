/*****************************************************************************!
 * Global Headers
 *****************************************************************************/
#include <QtCore>
#include <QtGui>
#include <QWidget>

/*****************************************************************************!
 * Local Headers
 *****************************************************************************/
#include "TrackNumber.h"

/*****************************************************************************!
 * Function : TrackNumber
 *****************************************************************************/
TrackNumber::TrackNumber
(int InNumber) : QWidget()
{
  Number = InNumber;
}

/*****************************************************************************!
 * Function : ~TrackNumber
 *****************************************************************************/
TrackNumber::~TrackNumber
()
{
}

/*****************************************************************************!
 * Function : GetNumber
 *****************************************************************************/
int
TrackNumber::GetNumber(void)
{
  return Number;  
}

/*****************************************************************************!
 * Function : SetNumber
 *****************************************************************************/
void
TrackNumber::SetNumber
(int InNumber)
{
  Number = InNumber;  
}

/*****************************************************************************!
 * Function : ToString
 *****************************************************************************/
QString
TrackNumber::ToString(void)
{
  QString                               st;
  int                                   major;
  int                                   minor;
  int                                   release;

  major = Number / 10000;
  minor = Number % 10000;
  minor /= 100;
  release = Number % 100;

  st = QString("%1.%2.%3").arg(major).arg(minor).arg(release);
  return st;
}
